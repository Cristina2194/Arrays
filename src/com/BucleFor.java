package com;

public class BucleFor {
	/*El bucle for proporciona una forma concisa de escribir la estructura de bucle. 
	 A diferencia de un ciclo while, una sentencia for consume la inicialización, 
	 la condición y el incremento/decremento en una línea, proporcionando así 
	 una estructura de bucle más corta y fácil de depurar.*/

	public static void main(String[] args) {
		
		int valor=0;
		{
		for(int i=1; i<7; i++) 
		
		System.out.println(valor+i);
		}
		
		

	}

}
